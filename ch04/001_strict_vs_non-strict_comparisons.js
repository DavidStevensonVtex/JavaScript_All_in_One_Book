if ( 0 == "0")
{
    console.log('0 is equal to "0" via ==');
}
else
{
    console.log('0 is not equal to "0" via ==');
}

if ( 0 === "0" )
{
    console.log('0 is equal to "0" via ===');
}
else
{
    console.log('0 is not equal to "0" via ===');
}

// 0 is equal to "0" via ==
// 0 is not equal to "0" via ===